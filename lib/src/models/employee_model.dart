class Employee {
  final String name;
  final String email;
  final String phone;
  final String department;
  final String jobTitle;
  final String dateOfBirth;

  Employee({
    required this.name,
    required this.email,
    required this.phone,
    required this.department,
    required this.jobTitle,
    required this.dateOfBirth,
  });
}
